// Concrete class representing the Pandavs
class Pandav extends Person {
    // Override the obedience method
    @Override
    void obey() {
        System.out.println("Pandav obeys the rules and elders.");
    }

    // Override the kindness method
    @Override
    void showKindness() {
        System.out.println("Pandav shows kindness.");
    }
}
